def plot_celltype_f1_heatmap(results_df, output_dir, PNG_DPI=DEFAULT_PNG_DPI):
    """
    Create a heatmap with celltypes as rows and probe panels (datasets) as columns
    showing F1-scores from the evaluation results.
    
    Parameters:
    -----------
    results_df : DataFrame
        Results from evaluate_celltype_identification() with columns:
        ['dataset', 'celltype', 'precision', 'recall', 'f1-score', 'support', 'accuracy']
    output_dir : str
        Directory to save plot
    DEFAULT_PNG_DPI : int
        Resolution for saved figure
        
    Returns:
    --------
    str
        Path to saved figure
    """
    # Filter for rows that have celltype data (not the summary rows where celltype is NaN)
    celltype_data = results_df[results_df[COL_CELLTYPE].notna() & results_df[COL_F1_SCORE].notna()].copy()
    
    if celltype_data.empty:
        logging.warning("No celltype-specific F1-score data found!")
        return None
    
    logging.info(f"Datasets found: {sorted(celltype_data['dataset'].unique())}")
    logging.info(f"Celltypes found: {sorted(celltype_data['celltype'].unique())}")
    
    # Create mapping of original dataset names to display names for axis labels
    # Use extract_display_name_from_dataset to include ratio information
    import re
    
    # Check if we have multiple panel sizes - if so, include size in display names
    panel_sizes = set()
    for dataset in celltype_data['dataset'].unique():
        size_match = re.search(r'_(\d+)(?:_|$)', dataset)
        if size_match:
            panel_sizes.add(int(size_match.group(1)))
    
    include_size = len(panel_sizes) > 1
    if include_size:
        logging.info(f"Multiple panel sizes detected: {sorted(panel_sizes)} - including size in labels")
    
    dataset_display_names = {}
    for dataset in celltype_data['dataset'].unique():
        factor_range_mode = bool(re.search(r'_\d+factors_', dataset))
        display_name = extract_display_name_from_dataset(dataset, factor_range_mode=factor_range_mode, include_size=include_size)
        dataset_display_names[dataset] = display_name
    
    # Create pivot table: celltypes as rows (index), datasets as columns, f1-score as values
    heatmap_data = celltype_data.pivot_table(
        index='celltype',
        columns='dataset', 
        values='f1-score',
        aggfunc='first'  # Use first value if duplicates exist
    )
    
    # Rename columns to use display names
    heatmap_data = heatmap_data.rename(columns=dataset_display_names)
    
    # Sort celltypes by mean F1-score across all datasets (descending)
    # This puts the best-performing celltypes at the top
    heatmap_data['mean_f1'] = heatmap_data.mean(axis=1)
    heatmap_data = heatmap_data.sort_values('mean_f1', ascending=False)
    heatmap_data = heatmap_data.drop('mean_f1', axis=1)  # Remove helper column
    
    # Create the heatmap with appropriate figure size based on data dimensions
    n_celltypes = len(heatmap_data)
    n_datasets = len(heatmap_data.columns)
    
    # Scale figure size based on number of items
    fig_width = max(10, n_datasets * 0.8)
    fig_height = max(8, n_celltypes * 0.4)
    
    plt.figure(figsize=(fig_width, fig_height))
    
    # Create heatmap
    ax = sns.heatmap(
        heatmap_data,
        annot=True,           # Show F1-score values in cells
        fmt='.3f',            # Format to 3 decimal places
        cmap='viridis',       # Color scheme
        linewidths=0.5,       # Cell borders
        vmin=0,               # Minimum value for color scale
        vmax=1.0,             # Maximum value for color scale
        cbar_kws={'label': 'F1-Score'}
    )
    
    # Customize the plot
    plt.title('F1-Score per cell type', fontsize=14, pad=20)
    plt.xlabel('Probe Panel (Dataset)', fontsize=12, labelpad=10)
    plt.ylabel('Cell Type', fontsize=12, labelpad=10)
    
    # Rotate x-axis labels for better readability
    plt.xticks(rotation=45, ha='right', fontsize=9)
    plt.yticks(rotation=0, fontsize=9)
    
    # Adjust layout
    plt.tight_layout()
    
    # Save the heatmap
    heatmap_path = os.path.join(output_dir, 'celltype_f1score_heatmap.png')
    plt.savefig(heatmap_path, dpi=DEFAULT_PNG_DPI, bbox_inches='tight')
    
    # Also save as PDF
    pdf_path = os.path.join(output_dir, 'celltype_f1score_heatmap.pdf')
    plt.savefig(pdf_path, bbox_inches='tight')
    
    # Save the pivot table as CSV
    csv_output_path = os.path.join(output_dir, 'celltype_f1score_matrix.csv')
    heatmap_data.to_csv(csv_output_path)
    
    plt.close()
    
    logging.info(f"Heatmap saved to: {heatmap_path}")
    logging.info(f"Data matrix saved to: {csv_output_path}")
    logging.info(f"Heatmap dimensions: {n_celltypes} celltypes × {n_datasets} probe panels")
    
    return heatmap_path


