def _dataset_line_style(dataset_name: str) -> str:
    """Return a deterministic line style based on panel size encoded in dataset name."""
    match = re.search(r"_(\d+)(?:_|$)", str(dataset_name))
    if not match:
        return "-"
    size = int(match.group(1))
    if size == 100:
        return "-"
    if size == 200:
        return "--"
    if size == 500:
        return "-."
    return ":"

