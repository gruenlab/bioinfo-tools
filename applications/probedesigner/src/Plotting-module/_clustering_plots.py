def create_feature_plots(ref_data, gene_lists, output_dir, logger, PNG_DPI=DEFAULT_PNG_DPI):
    """
    Create feature plots for each gene in each probeset to visualize expression patterns.
    
    Parameters:
    -----------
    ref_data : AnnData
        Reference dataset with all genes
    gene_lists : dict
        Dictionary of gene lists to visualize
    output_dir : str
        Base directory to save the feature plots
    logger : logging.Logger
        Logger instance
    DEFAULT_PNG_DPI : int
        Resolution for saved PNG images (default: 300)
    """
    # Create directory for feature plots
    os.makedirs(output_dir, exist_ok=True)
    featureplots_dir = output_dir
    
    logging.info(f"Creating feature plots for genes in each probeset...")
    
    # Prepare UMAP embedding if not already computed
    if 'X_umap' not in ref_data.obsm:
        logging.info("Computing UMAP embedding for reference data...")
        try:
            sc.pp.neighbors(ref_data)
            sc.tl.umap(ref_data)
            logging.info("UMAP embedding computed successfully")
        except Exception as e:
            logging.error(f"Error computing UMAP embedding: {e}")
            return
    
    # Process only DT-NMF and XGB-NMF gene lists
    # Look for probeset names that contain decision tree or XGBoost methods with NMF ratios
    target_probesets = []
    for name in gene_lists.keys():
        # Target DT probesets with NMF ratios (e.g., "DT_Method_A_25-NMF_75-DEG_100")
        if name.startswith('DT_Method_') and ('NMF' in name):
            target_probesets.append(name)
        # Target XGB probesets with NMF ratios (if available)
        elif name.startswith('XGB_') and ('NMF' in name):
            target_probesets.append(name)
        # Also include legacy naming conventions
        elif name in ['dt_genes', 'xgb_genes']:
            target_probesets.append(name)
    
    if not target_probesets:
        logging.warning("No DT-NMF or XGB-NMF probesets found for feature plots")
        return
    
    logging.info(f"Found {len(target_probesets)} DT/XGB-NMF probesets for feature plots: {target_probesets}")
    
    # Process each target gene list
    for name in target_probesets:
        gene_list = gene_lists[name]
        logging.info(f"Creating feature plots for {name}...")
        
        # Create directory for this gene list
        geneset_dir = os.path.join(featureplots_dir, name)
        os.makedirs(geneset_dir, exist_ok=True)
        
        # Check if genes is an iterable and not a string
        if not hasattr(gene_list, '__iter__') or isinstance(gene_list, str) or isinstance(gene_list, sc.AnnData):
            logging.warning(f"Skipping {name} - not an iterable gene list: {type(gene_list)}")
            continue
        
        # Filter for genes that exist in the reference data
        valid_genes = [g for g in gene_list if g in ref_data.var_names]
        
        if len(valid_genes) != len(gene_list):
            logging.warning(f"Only {len(valid_genes)} out of {len(gene_list)} genes from {name} found in reference data")
        
        if not valid_genes:
            logging.warning(f"No valid genes found for {name}, skipping")
            continue
        
        # Cap the number of genes to plot to avoid memory issues (max 20 genes for feature plots)
        plot_genes = valid_genes[:100]
        if len(valid_genes) > 100:
            logging.warning(f"Limiting to first 100 genes for {name} to avoid memory issues and reduce processing time")
                
        # Create individual plots for each gene
        for i, gene in enumerate(plot_genes):
            try:
                plt.figure(figsize=(8, 6))
                sc.pl.umap(ref_data, color=gene, show=False, title=f"{gene} expression", size=40, color_map='viridis')
                plt.tight_layout()
                
                gene_path = os.path.join(geneset_dir, f"{name}_{gene}_featureplot.png")
                plt.savefig(gene_path, dpi=DEFAULT_PNG_DPI, bbox_inches='tight')
                plt.close()
                
                # Log progress periodically
                if (i + 1) % 5 == 0 or i == len(plot_genes) - 1:
                    logging.info(f"Created feature plots for {i+1}/{len(plot_genes)} genes in {name}")
                    
            except Exception as e:
                logging.error(f"Error creating feature plot for gene {gene}: {e}")
                plt.close()  # Close the current figure
                continue
            
            # Force garbage collection every 5 plots
            if (i + 1) % 5 == 0:
                gc.collect()
    
    logging.info(f"Feature plots creation completed. Results saved to: {featureplots_dir}")


